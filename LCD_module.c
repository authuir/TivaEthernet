void LCD_pin_init()
{
	//Enable GPIO B,C,D,E
	ROM_SysCtlPeripheralEnable (SYSCTL_PERIPH_GPIOC);
	ROM_SysCtlPeripheralEnable (SYSCTL_PERIPH_GPIOE);

	//PC5<-->SCLK
	//PC4<-->SDA
	//PE4<-->CD
	//RESET  直接接到了电路复位上去
	//PE5<-->CS

	ROM_GPIOPinTypeGPIOOutput (GPIO_PORTC_BASE, GPIO_PIN_5);  //PC5
	ROM_GPIOPinTypeGPIOOutput (GPIO_PORTC_BASE, GPIO_PIN_4);  //PC4
	ROM_GPIOPinTypeGPIOOutput (GPIO_PORTE_BASE, GPIO_PIN_4);  //PE4
	ROM_GPIOPinTypeGPIOOutput (GPIO_PORTE_BASE, GPIO_PIN_5);  //PE5

	ROM_GPIOPinWrite (GPIO_PORTC_BASE, GPIO_PIN_5 | GPIO_PIN_4, 0x00);	//Set default value
	ROM_GPIOPinWrite (GPIO_PORTE_BASE, GPIO_PIN_5 | GPIO_PIN_4, 0x00);
}
void LCD_initialization()
{
	CS_EQ_0();
	LCD_transfer_command(0xe2); /*软重启*/
	ROM_SysCtlDelay (300);
	LCD_transfer_command(0x2c);
	LCD_transfer_command(0x2e);
	LCD_transfer_command(0x2f); 
	LCD_transfer_command(0x23); /*粗调对比度，可设置范围 0x20～0x27*/
	LCD_transfer_command(0x81); /*微调对比度*/
	LCD_transfer_command(0x28); /*0x1a,微调对比度的值，可设置范围 0x00～0x3f*/
	LCD_transfer_command(0xa2); /*1/9 偏压比（bias）*/
	LCD_transfer_command(0xc8); /*行扫描顺序：从上到下*/
	LCD_transfer_command(0xa0); /*列扫描顺序：从左到右*/
	LCD_transfer_command(0x40); /*起始行：第一行开始*/
	LCD_transfer_command(0xaf); /*开显示*/
	CS_EQ_1();
}

void LCD_transfer_command(uint8_t com)
{
	uint8_t i;
	CS_EQ_0();
	ROM_SysCtlDelay (delaytime);
	CD_EQ_0();
	ROM_SysCtlDelay (delaytime);
	for (i = 0; i < 8; i++)
	{
		SCK_EQ_0();
		ROM_SysCtlDelay (delaytime);
		if (com & 0x80)
		{
			SDA_EQ_1();
			ROM_SysCtlDelay (delaytime);
			com <<= 1;
		}
		else
		{
			SDA_EQ_0();
			ROM_SysCtlDelay (delaytime);
			com <<= 1;
		}
		SCK_EQ_1();
		ROM_SysCtlDelay (delaytime*10);
	}
}

void LCD_transfer_data(uint8_t data)
{
	uint8_t i;
	CS_EQ_0();
	ROM_SysCtlDelay (delaytime);
	CD_EQ_1();
	ROM_SysCtlDelay (delaytime);
	for (i = 0; i < 8; i++)
	{
		SCK_EQ_0();
		ROM_SysCtlDelay (delaytime);
		if (data & 0x80)
		{
			SDA_EQ_1();
			ROM_SysCtlDelay (delaytime);
			data <<= 1;
		}
		else
		{
			SDA_EQ_0();
			ROM_SysCtlDelay (delaytime);
			data <<= 1;
		}
		SCK_EQ_1();
		ROM_SysCtlDelay (delaytime*10);
	}
}

void LCD_address(uint8_t page, uint8_t column)
{
	/*
	 * 这里是按照自然思路设计的page与column，第1页1行就直接带入参数1,1即可
	 */
	ROM_SysCtlDelay (500);
	;
	column = column - 1;
	page = page - 1;
	LCD_transfer_command(0xb0 + page); /*设置页地址。每页是 8 行。一个画面的 64 行被分成 8 个页。我们平常所说的第 1 页，在 LCD 驱动 IC 里是第 0 页，所以在这里减去 1*/
	LCD_transfer_command(((column >> 4) & 0x0f) + 0x10);  //设置列地址的高 4 位
	LCD_transfer_command(column & 0x0f);     //设置列地址的低 4 位
}

void LCD_clear_screen()
{
	uint8_t i, j;
	CS_EQ_0();
	ROM_SysCtlDelay (500);
	;
	for (i = 0; i < 9; i++)
	{
		LCD_address(1 + i, 0);
		for (j = 0; j < 132; j++)
		{
			LCD_transfer_data(0x00);
		}
	}
	ROM_SysCtlDelay (500);
	CS_EQ_1();
}

void LCD_full_display()
{
	uint8_t i, j;
	CS_EQ_0();
	ROM_SysCtlDelay (500);
	;
	for (i = 0; i < 8; i++)
	{
		LCD_address(i + 1, 1);
		for (j = 0; j < 128; j++)
		{
			LCD_transfer_data(0xFF);
		}
	}
	CS_EQ_1();
}

